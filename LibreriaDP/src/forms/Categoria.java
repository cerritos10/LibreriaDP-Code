    void listar(){
        String sql="select*from categoria";
        try {
            cn = con.connx();
            st=cn.createStatement();
            rs=st.executeQuery(sql);
            Object[]categoria=new Object[2];
            modelo=(DefaultTableModel)tableCategoria.getModel();
            while(rs.next()){
                categoria[0]=rs.getInt("id_categoria");
                categoria[1]=rs.getString("nombre_categoria");
                modelo.addRow(categoria);
            }
            tableCategoria.setModel(modelo);
        } catch (Exception e) {          
        }                 
    }
    void agregar(){
        String categ = txtCategoria.getText();
        int AutoIncr = 1;
        if(categ.equals("")){
            JOptionPane.showMessageDialog(null,"Ingrese Categoria");
        } else{
            String sql="insert into categoria (nombre_categoria) values('"+categ+"')";
            try {
                cn=con.connx();
                st=cn.createStatement();
                st.executeUpdate(sql);
                limpiartabla();
                JOptionPane.showMessageDialog(null,"Categoria Ingresada");
            } catch (Exception e){
            }
        }
    }
    void limpiartabla(){
        for(int i=0;i<=tableCategoria.getRowCount();i++){
            modelo.removeRow(i);
            i=i-1;
        }
    }

