    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Existencias modSql=new Existencias(); 
        if (txtCategoria.getText().equals("")){
            JOptionPane.showMessageDialog(this, "No dejar el campo de categoria vacio");
            
        }
        else{
        if (modSql.ExistCategori(txtCategoria .getText())==0) {
            agregar();
        this.txtCategoria.setText("");
        listar();
        } else {
            JOptionPane.showMessageDialog(this, "La categoria ya existe");
        }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

