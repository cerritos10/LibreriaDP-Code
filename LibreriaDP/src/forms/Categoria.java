    void eliminar(){
        int filaseleccionada=tableCategoria.getSelectedRow();
        if(filaseleccionada==-1){
            JOptionPane.showMessageDialog(null,"Seleccione una categoria");
        } else{
            String sql="delete from categoria where id_categoria="+id;
            try{
                cn=con.connx();
                st=cn.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null,"Categoria Eliminada");
                limpiartabla();
            }catch (Exception e){
                
            }
        }
    }
    void nuevo(){
        txtCategoria.setText("");
        txtCategoria.requestFocus();
        
    }
    void modificar(){
        String cate=txtCategoria.getText();
        String sql="update categoria set nombre_categoria='"+cate+"' where id_categoria="+id;
        if (cate.equals("")){
            JOptionPane.showMessageDialog(null,"Debe ingresar la Categoria");
            limpiartabla();
        }else{
            try{
                cn=con.connx();
                st=cn.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null,"Categoria Actualizada");
                limpiartabla();
            }catch (Exception e){                
            }
        }
    }
